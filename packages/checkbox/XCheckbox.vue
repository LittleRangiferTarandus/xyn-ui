<template>
  <label class="xyn-checkbox" :class="{' is-checked':currentValue} " 
  >
    <span class="xyn-checkbox_input">
      <span class="xyn-checkbox_inner"></span>
      <input type="checkbox"
      class="xyn-checkbox_original"
      :name="name"
      v-model="currentValue"
      
      >
    </span>
    <span class="xyn-checkbox_label">
      <slot></slot>
      <template v-if="!$slots.default">
        {{label}}
      </template>
    </span>
  </label>
</template>

<script lang="ts">
import { defineComponent, inject, ref, watch } from 'vue'
import { CheckboxGroup } from '../types/component'

export default defineComponent({
  name:"XynCheckbox",
    props: {
      label: {
        type: String,
        defualt: ''
      },
      modelValue: {
        type:Boolean,
        default: false
      },
      name: {
        type: String,
        defualt: ''
      }
    },
    setup(props,context) {
      const currentValue = ref (props.modelValue)

      const checkboxGroup:CheckboxGroup|undefined = inject("CheckboxGroup")
      if(checkboxGroup){
        currentValue.value=checkboxGroup.getSelect().includes(props.label)
        context.emit("update:modelValue",currentValue)
      }
      watch(currentValue,()=>{
        
        checkboxGroup?.addSelect(props.label)
        context.emit("update:modelValue",currentValue)
        context.emit("change",!currentValue,currentValue)
      })
      return{
        checkboxGroup,currentValue
      }
    }
})
</script>


